    private void showData(String s) throws SQLException {

        tblPhongBan.setModel(new javax.swing.table.DefaultTableModel(
                new Object[][]{},
                new String[]{
                    "Mã Phòng", "Tên Phòng Ban"
                }
        ));
        if (conn != null) {
            try {
                String sql = "select map,tenp from PHONGBAN where map like '%" + s + "%' or tenp like '%" + s + "%' ";
                //String sql="select map,tenp from PHONGBAN";
                Statement stmt = conn.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_READ_ONLY);
                rs = stmt.executeQuery(sql);

                while (rs.next()) {
                    String mapb = rs.getString("map");
                    String tenpb = rs.getString("tenp");
                    String a[] = new String[]{
                        mapb, tenpb
                    };
                    System.out.println("" + a);
                    ((DefaultTableModel) tblPhongBan.getModel()).addRow(a);

                }
            } catch (SQLException ex) {
                Logger.getLogger(FPhongban.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

