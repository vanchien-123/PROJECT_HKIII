    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (flag == 1) {
            if (tftMaLTB.getText().equals("") || tftTenLTB.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Please Enter All Data!");
            } else {
                try {
                    // TODO add your handling code here
                    String maloai = tftMaLTB.getText();
                    String tenloai = tftTenLTB.getText();
                    String sql = "insert into LOAITB(maloai,tenloai) values(N'" + maloai + "',N'" + tenloai + "')";
                    Statement stmt = conn.createStatement();
                    int kq = stmt.executeUpdate(sql);
                    if (kq > 0) {
                        JOptionPane.showMessageDialog(null, "Insert successfully");
                        showData("");
                    } else {
                        JOptionPane.showMessageDialog(null, "Insert not successfully");
                    }
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, ex);
                }
            }
        } else if (flag == 2) {
            try {
                // TODO add your handling code here:
                String maloai = tftMaLTB.getText();
                String tenloai = tftTenLTB.getText();
                String sql = "UPDATE LOAITB set tenloai=N'" + tenloai + "' where maloai='" + maloai.trim() + "'";
                Statement stmt = conn.createStatement();
                int kq = stmt.executeUpdate(sql);
                if (kq > 0) {
                    JOptionPane.showMessageDialog(null, "Update susscessfully");
                    showData("");
                    System.out.println("Được");
                    System.out.println(stmt);
                    System.out.println(sql);
                    System.out.println(kq);
                } else {
                    System.out.println("Ko Được");
                    System.out.println(stmt);
                    System.out.println(sql);
                    System.out.println(kq);
                    JOptionPane.showMessageDialog(null, "Update not susscessfully");
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        }
        btnSave.setEnabled(false);
        btnAdd.setEnabled(true);
    }//GEN-LAST:event_btnSaveActionPerformed

