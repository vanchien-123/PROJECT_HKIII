    private void showData(String s) throws SQLException {

        tblLoaiTB.setModel(new javax.swing.table.DefaultTableModel(
                new Object[][]{},
                new String[]{
                    "Mã Loại", "Tên Loại"
                }
        ));
        if (conn != null) {
            try {
                String sql = "select maloai,tenloai from LOAITB where maloai like '%" + s + "%' or tenloai like '%" + s + "%'";
                //String sql="select map,tenp from PHONGBAN";
                Statement stmt = conn.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_READ_ONLY);
                rs = stmt.executeQuery(sql);

                while (rs.next()) {
                    String malaoi = rs.getString("maloai");
                    String tenloai = rs.getString("tenloai");
                    String a[] = new String[]{
                        malaoi, tenloai
                    };
                    System.out.println("" + a);
                    ((DefaultTableModel) tblLoaiTB.getModel()).addRow(a);

                }
            } catch (SQLException ex) {
                Logger.getLogger(FLoaiTB.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
    
